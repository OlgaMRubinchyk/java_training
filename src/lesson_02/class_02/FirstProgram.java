package lesson_02.class_02;
public class FirstProgram {

    /**
     * zsgzdgf
     * zdfg
     * zdfg
     * zdf
     * @param args
     */

    public static void main(String[] args) {
        System.out.print("Hello ");
        System.out.println("Java! ");
        System.out.println("Goodbye! ");

        int age = 25; // initialized age variable
        double price = 23.5; // initialized price variable
        System.out.println(age);
        System.out.println(price);

        /*
            Extra
            long text
            !
         */

        char char1 = 'e'; //single character
        char char2 = 251; // ASCII code
        char char3 = '\u0065'; // Unicode

        System.out.println(char1);
        System.out.println(char2);
        System.out.println(char3);

        boolean isMe = true;
        System.out.println(isMe);

        // Extra comment
        long longNumber = 123326345675675367L; //use L at the end
        System.out.println(longNumber);

        float floatNumber = 1.25f; // use f at the end
        System.out.println(floatNumber);

    }
}